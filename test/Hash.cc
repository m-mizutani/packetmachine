#include "./gtest/gtest.h"
#include "../src/utils/hash.hpp"

TEST(Hash, basic) {
  char d1[] = "abcdefgh";
  char d2[] = "abcdefXX";
  EXPECT_NE(pm::hash32(d1, 4), pm::hash32(d1, 5));
  EXPECT_EQ(pm::hash32(d1, 6), pm::hash32(d2, 6));
  EXPECT_NE(pm::hash32(d1, 7), pm::hash32(d2, 7));
}


